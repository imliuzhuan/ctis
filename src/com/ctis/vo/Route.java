package com.ctis.vo;

/**
 * 该vo类封装了页面显示需要的线路信息
 * @author liqq
 *
 */
public class Route {
	private int transTime;
	private String route;

	public int getTransTime() {
		return transTime;
	}

	public void setTransTime(int transTime) {
		this.transTime = transTime;
	}

	public String getRoute() {
		return route;
	}

	public void setRoute(String route) {
		this.route = route;
	}
}
